/// Generate all code from the loaded schemas and config.
/// Returns a map of file_path → file_content.
pub fn generate(
    cfg: &Config,
    schemas: &HashMap<String, Schema>,
) -> Result<BTreeMap<String, String>, String> {
    let mut files = BTreeMap::new();

    // Sort schema IDs for deterministic output.
    let mut schema_ids: Vec<&str> = schemas.keys().map(|s| s.as_str()).collect();
    schema_ids.sort();

    // Generate code for each schema.
    for &nsid in &schema_ids {
        let schema = &schemas[nsid];
        let pkg = match cfg.find_package(nsid) {
            Some(p) => p,
            None => continue, // Not in a configured package
        };

        let caller_module = format!("crate::{}", pkg.module);
        let ctx = GenContext {
            schema,
            cfg,
            schemas,
            caller_module: &caller_module,
        };

        let code = match gen_schema_file(&ctx) {
            Ok(c) => c,
            Err(e) => return Err(format!("{nsid}: {e}")),
        };

        if code.is_empty() {
            continue;
        }

        let fname = util::schema_file_name(nsid);
        let path = format!("{}/{fname}", pkg.out_dir);
        files.insert(path, code);
    }

    // Generate module hierarchy files.
    // Only include schemas that actually produced output files.
    let generated_ids: Vec<&str> = files
        .keys()
        .filter_map(|path| {
            // Reverse-map file path to schema ID
            schema_ids
                .iter()
                .find(|&&nsid| {
                    let pkg = cfg.find_package(nsid);
                    if let Some(p) = pkg {
                        let fname = util::schema_file_name(nsid);
                        let expected = format!("{}/{fname}", p.out_dir);
                        path == &expected
                    } else {
                        false
                    }
                })
                .copied()
        })
        .collect();
    let packages: Vec<(&str, &str)> = cfg
        .packages
        .iter()
        .map(|p| (p.prefix.as_str(), p.out_dir.as_str()))
        .collect();
    let mod_files = gen_module::gen_module_files(&generated_ids, &packages);
    files.extend(mod_files);

    // Determine which top-level modules exist from package config.
    let mut top_modules: Vec<&str> = cfg
        .packages
        .iter()
        .filter_map(|p| p.module.split("::").next())
        .collect::<std::collections::BTreeSet<&str>>()
        .into_iter()
        .collect();
    top_modules.sort();

    // Generate shared types (lib.rs).
    let lib_content = gen_shared::gen_shared_types(&top_modules);
    files.insert("src/api/mod.rs".to_string(), lib_content);

    Ok(files)
}

/// Generate the complete source file for one schema.
fn gen_schema_file(ctx: &GenContext<'_>) -> Result<String, String> {
    let schema = ctx.schema;
    let mut sections = Vec::new();

    // Sort defs for deterministic output.
    let mut def_names: Vec<&String> = schema.defs.keys().collect();
    def_names.sort();

    for def_name in &def_names {
        let def_name_str: &str = def_name;
        let def = &schema.defs[def_name_str];

        let code = gen_def(ctx, def_name_str, def)?;
        if !code.is_empty() {
            sections.push(code);
        }
    }

    if sections.is_empty() {
        return Ok(String::new());
    }

    let mut out = String::from("// Code generated by lexgen. DO NOT EDIT.\n\n");
    out.push_str(&sections.join("\n\n"));
    out.push('\n');

    Ok(out)
}

/// Generate code for a single def.
fn gen_def(ctx: &GenContext<'_>, def_name: &str, def: &Def) -> Result<String, String> {
    match def {
        Def::Record(rec) => {
            gen_struct::gen_record(ctx, def_name, &rec.record, rec.description.as_deref())
        }
        Def::Object(obj) => gen_struct::gen_object(ctx, def_name, obj),
        Def::Query(q) => gen_endpoint::gen_query(ctx, def_name, q),
        Def::Procedure(p) => gen_endpoint::gen_procedure(ctx, def_name, p),
        Def::Token(t) => Ok(gen_struct::gen_token(
            &ctx.schema.id,
            def_name,
            t.description.as_deref(),
        )),
        Def::StringDef(s) => Ok(gen_struct::gen_string_def(
            &ctx.schema.id,
            def_name,
            s.description.as_deref(),
        )),
        Def::ArrayDef(arr) => gen_array_def(ctx, def_name, arr),
        Def::Subscription(_) => {
            // Skip subscription client generation for now (Task 9+).
            // Still generate any associated object defs in the same schema.
            Ok(String::new())
        }
        Def::BooleanDef(_) | Def::IntegerDef(_) | Def::BytesDef(_) | Def::Unknown => {
            Ok(String::new())
        }
    }
}

/// Generate code for a top-level array def (e.g. `app.bsky.actor.defs#preferences`).
fn gen_array_def(
    ctx: &GenContext<'_>,
    def_name: &str,
    arr: &ArrayTypeDef,
) -> Result<String, String> {
    let type_name = util::type_name(&ctx.schema.id, def_name);
    let mut out = String::new();
    let mut extras = Vec::new();

    // If items is a union, generate the union type.
    match &arr.items {
        FieldSchema::Union {
            refs,
            closed,
            description,
            ..
        } => {
            let elem_name = format!("{type_name}Item");
            let union_code =
                gen_union::gen_union(ctx, &elem_name, refs, *closed, description.as_deref())?;
            extras.push(union_code);
            if let Some(desc) = &arr.description {
                writeln!(out, "/// {}", single_line(desc)).ok();
            } else {
                writeln!(
                    out,
                    "/// {type_name} is an array type from {}.",
                    ctx.schema.id
                )
                .ok();
            }
            writeln!(out, "pub type {type_name} = Vec<{elem_name}>;").ok();
        }
        other => {
            let (elem_type, field_extras) =
                gen_struct::resolve_field_type(ctx, &type_name, "item", other, true)?;
            extras.extend(field_extras);
            if let Some(desc) = &arr.description {
                writeln!(out, "/// {}", single_line(desc)).ok();
            } else {
                writeln!(
                    out,
                    "/// {type_name} is an array type from {}.",
                    ctx.schema.id
                )
                .ok();
            }
            writeln!(out, "pub type {type_name} = Vec<{elem_type}>;").ok();
        }
    }

    let mut result = String::new();
    for extra in extras {
        result.push_str(&extra);
        result.push_str("\n\n");
    }
    result.push_str(&out);
    Ok(result)
}

fn single_line(s: &str) -> String {
    let mut result = String::with_capacity(s.len());
    for word in s.replace('\n', " ").split_whitespace() {
        if !result.is_empty() {
            result.push(' ');
        }
        if word.starts_with("http://") || word.starts_with("https://") {
            result.push('<');
            result.push_str(word);
            result.push('>');
        } else {
            for ch in word.chars() {
                match ch {
                    '<' => result.push_str("&lt;"),
                    '>' => result.push_str("&gt;"),
                    _ => result.push(ch),
                }
            }
        }
    }
    result
}

#[cfg(test)]
#[allow(clippy::unwrap_used, clippy::panic)]
mod tests {
    use super::*;
    use crate::config::Config;
    use crate::loader;
    use std::path::Path;

    fn test_data() -> (Config, HashMap<String, Schema>) {
        let cfg = Config::load(Path::new("../../lexgen.json")).unwrap();
        let schemas = loader::load_schemas(Path::new("../../lexicons")).unwrap();
        (cfg, schemas)
    }

    #[test]
    fn generate_produces_files() {
        let (cfg, schemas) = test_data();
        let files = generate(&cfg, &schemas).unwrap();

        // Check that key files exist
        assert!(files.contains_key("src/api/mod.rs"), "missing lib.rs");
        assert!(
            files.keys().any(|k| k.contains("feed_post.rs")),
            "missing feed_post.rs"
        );
        assert!(
            files.keys().any(|k| k.contains("repo_strong_ref.rs")),
            "missing repo_strong_ref.rs"
        );
        assert!(
            files.contains_key("src/api/app/mod.rs"),
            "missing app/mod.rs"
        );
    }

    #[test]
    fn generated_lib_has_modules() {
        let (cfg, schemas) = test_data();
        let files = generate(&cfg, &schemas).unwrap();
        let lib = &files["src/api/mod.rs"];
        assert!(lib.contains("pub mod app;"), "lib:\n{lib}");
        assert!(lib.contains("pub mod com;"), "lib:\n{lib}");
    }

    #[test]
    fn strong_ref_file_compiles_looking() {
        let (cfg, schemas) = test_data();
        let files = generate(&cfg, &schemas).unwrap();
        let key = files
            .keys()
            .find(|k| k.contains("repo_strong_ref.rs"))
            .unwrap();
        let code = &files[key];
        assert!(code.contains("pub struct RepoStrongRef"), "code:\n{code}");
        assert!(code.contains("pub uri:"), "code:\n{code}");
        assert!(code.contains("pub cid:"), "code:\n{code}");
    }
}
